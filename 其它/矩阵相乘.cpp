#include<stdio.h>
int main()
{
	int a[3][3],b[3][3],c[3][3]={0};
	int i,j,k;
	scanf("%d %d %d %d %d %d %d %d %d",&a[0][0],&a[0][1],&a[0][2],&a[1][0],&a[1][1],&a[1][2],&a[2][0],&a[2][1],&a[2][2]);
	scanf("%d %d %d %d %d %d %d %d %d",&b[0][0],&b[0][1],&b[0][2],&b[1][0],&b[1][1],&b[1][2],&b[2][0],&b[2][1],&b[2][2]);
    for(i=0;i<3;i++) 
	{ 
		for(j=0;j<3;j++) 
		{ for(k=0;k<3;k++) 
		{ 
			c[i][j]+=a[i][k]*b[k][j];
		}
		}
	}
    printf("%d %d %d \n%d %d %d \n%d %d %d \n",c[0][0],c[0][1],c[0][2],c[1][0],c[1][1],c[1][2],c[2][0],c[2][1],c[2][2]);
    return 0;
}